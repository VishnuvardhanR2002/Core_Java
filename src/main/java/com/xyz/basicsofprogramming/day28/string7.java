package com.xyz.basicsofprogramming.day28;

public class string7 {
	public static void main(String[] args) {
	String s = "RachepalliVishnuVardhan";
	System.out.println(s.toUpperCase());
	System.out.println(s.toLowerCase());
	System.out.println(s.startsWith("R"));
	System.out.println(s.endsWith("n"));
	System.out.println(s.contains(s));
	System.out.println(s.charAt(0));
	System.out.println(s.indexOf(0));
	System.out.println(s.getClass());
	System.out.println(s.concat("s"));
	System.out.println(s.hashCode());
	System.out.println(s.isBlank());
	System.out.println(s.isEmpty());
	System.out.println(s.lastIndexOf("d"));
	System.out.println(s.length());
	System.out.println(s.replace("a", "w"));
}
}
